// =    (Variable Assign - Assignment Operator)
// ==   (Comparison Operator)   check only value
// ===  (Comparison Operator)   check not only value but also DataType

// !=   check only value
// !==  check not only value but also DataType

// equal
console.log(1 == '1');  // true
console.log(1 === '1');  // false

// not equal
console.log(1 != '1'); // false
console.log(1 !== '1'); // true

// greater than / less than


console.log(5 > 6); // false
console.log(17 > 1); // true

console.log(18 < 20); // true
console.log(17 < 1); // false

// greater than equal / less than equal
console.log(10 >= 10) // true
console.log(5 <= 5); // true